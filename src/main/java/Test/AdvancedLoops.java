package Test;

public class AdvancedLoops {

	public static void main(String [] args) {
		
		
		
		
		int i =6;	
		
		
			//Outer loop
		
		
		// Implemented do while here even in case the condition is false it will still print the output once.
		do	
		{
			
			
			
		System.out.println("ok" + i);
		
		int j=1;
		
		//Inner loop
		while (j<=3)
			
			
		{
			System.out.println("okz" + j);
			j++;
		}
		
		
		
		i++;
		
		}while(i<=5);	

	}

}
